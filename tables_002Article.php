                          	<p><span>記者王大明 台北</span><cite class="date">03-23 14:02</cite><a href="tables_002.php" class="pull-right listBack">回個股列表</a></p>
                            <p><a href="#" class="atch-thumb"><img src="images/1301.jpg" alt="img"></a><figcaption itemprop="caption">台塑集團總裁王文淵。</figcaption></p>
                            
                            <p><strong>台塑 (1301-TW)</strong> 今 (23) 日董事會通過去年經會計師查核後財報，稅後純益為 393.93 億元，每股純益(EPS)6.19 元，並宣布每股擬配息 4.6 元，創 2011 年以來新高，配息率 74%，以今日收盤價 92.5 元計算，現金殖利率約 5%。</p>
                            <p>在台塑之前，集團四寶中的<strong>台化 (1326-TW)</strong> 已公布股利政策，台化去年 EPS 為 7.5 元，董事會通過每股擬配息 5.6 元，同創 2011 年以來新高，配息率也超過七成、達 75%，以今日收盤價 94.6 元計算，現金殖利率約 5.9%。</p>
                            <p>台塑四寶去年獲利王<strong>台塑化 (6505-TW)</strong>，去年 EPS 7.95 元，已公布擬配發現金股利 6 元，創歷年次高紀錄，配息率為 75%，今日收盤現金殖利率約 5.6%。</p>
                            <p>目前四寶當中，僅<strong>南亞 (1303-TW)</strong> 尚未公布股利政策，但觀察其他三寶配息率約 75% 左右，且現金殖利率約 5.5% 上下，法人依南亞去年 EPS 6.15 元推估，每股配息可望在 4.5 元左右。</p>
